# operations on vectors
# add a scalar to a vector
# multiply a vector by a scalar

# initialize a vector with values 1:10
vec1 <- c(1:10)

# add 3.8 to vec1
vec1 <- vec1 + 3.8

cat("vec1 after adding 3.8:", vec1)

# multiply vec1 by 5.2
vec1 <- vec1 * 5.2

cat("vec1 after multiplying by 5.2:", vec1)
